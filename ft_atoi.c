#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	sign;

	if (!nptr)
		return (0);
	i = 0;
	sign = 1;
	while (ft_isspace(*nptr))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (ft_isdigit(*nptr))
	{
		i = (i * 10) + (*nptr - '0');
		nptr++;
	}
	return (i * sign);
}
